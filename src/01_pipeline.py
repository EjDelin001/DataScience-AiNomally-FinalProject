print("Regions:", panel["region"].nunique())
print("Unique commodities:", panel["commodity"].nunique())
print("Nulls:\n", panel.isnull().sum())
print("\nSample:")
print(panel.head())

panel.to_csv(OUTPUTS / "panel_wfp_oni.csv", index=False)
