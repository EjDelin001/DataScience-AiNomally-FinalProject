# ══════════════════════════════════════════════════════════════
# STEP 5 — RESHAPE ONI
# ══════════════════════════════════════════════════════════════
print("\n" + "=" * 50)
print("STEP 5: Reshape ONI")
print("=" * 50)

season_to_month = {
    "DJF": 1, "JFM": 2, "FMA": 3, "MAM": 4,
    "AMJ": 5, "MJJ": 6, "JJA": 7, "JAS": 8,
    "ASO": 9, "SON": 10, "OND": 11, "NDJ": 12
}

oni_long = oni_raw.melt(id_vars="Year", value_vars=list(season_to_month.keys()),
                         var_name="season", value_name="oni")
oni_long = oni_long[oni_long["Year"] < 2026].copy()
oni_long = oni_long.dropna(subset=["oni"])
oni_long["month"] = oni_long["season"].map(season_to_month)
oni_long["date"] = pd.to_datetime(
    oni_long["Year"].astype(str) + "-" +
    oni_long["month"].astype(str).str.zfill(2) + "-01"
)
oni_long["oni"] = pd.to_numeric(oni_long["oni"], errors="coerce")
oni_long["enso_phase"] = "Neutral"
oni_long.loc[oni_long["oni"] >= 0.5,  "enso_phase"] = "El Nino"
oni_long.loc[oni_long["oni"] <= -0.5, "enso_phase"] = "La Nina"

oni_clean = oni_long[["date", "oni", "enso_phase"]].sort_values("date").reset_index(drop=True)

print("ONI shape:", oni_clean.shape)
print("Date range:", oni_clean["date"].min(), "->", oni_clean["date"].max())
print("ENSO phase counts:")
print(oni_clean["enso_phase"].value_counts())


# ══════════════════════════════════════════════════════════════
